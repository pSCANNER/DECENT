package edu.ucdavis.ehrc.web;

public class MatchingRecord {
	String edrsId;
	String firstName;
	String middleName;
	String lastName;
	String dob;
	String gender;
	String birthStateCode;
	String birthCountryCode;
	String decedentStateCode;
	String decedentCountryCode;
	String certifierFirstName;
	String certifierLastName;
	
	public String getEdrsId() {
		return edrsId;
	}
	public void setEdrsId(String edrsId) {
		this.edrsId = edrsId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBirthStateCode() {
		return birthStateCode;
	}
	public void setBirthStateCode(String birthStateCode) {
		this.birthStateCode = birthStateCode;
	}
	public String getBirthCountryCode() {
		return birthCountryCode;
	}
	public void setBirthCountryCode(String birthCountryCode) {
		this.birthCountryCode = birthCountryCode;
	}
	public String getDecedentStateCode() {
		return decedentStateCode;
	}
	public void setDecedentStateCode(String decedentStateCode) {
		this.decedentStateCode = decedentStateCode;
	}
	public String getDecedentCountryCode() {
		return decedentCountryCode;
	}
	public void setDecedentCountryCode(String decedentCountryCode) {
		this.decedentCountryCode = decedentCountryCode;
	}
	public String getCertifierFirstName() {
		return certifierFirstName;
	}
	public void setCertifierFirstName(String certifierFirstName) {
		this.certifierFirstName = certifierFirstName;
	}
	public String getCertifierLastName() {
		return certifierLastName;
	}
	public void setCertifierLastName(String certifierLastName) {
		this.certifierLastName = certifierLastName;
	}
	
}
